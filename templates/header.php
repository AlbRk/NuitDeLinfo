		<h3>Organisme</h3>
			<p>Refuge sans Frontières est un site réalisé par des étudiants en Informatique et Information et Communication
			de Paris Descartes, dans le cadre de la Nuit de l’Info. Ce site a pour vocation de recenser les personnes entrant
			dans des camps de réfugiés, de retrouver des proches, de donner des informations. Il permet aux services de santé
			de mieux se coordonner afin de limiter au mieux la contagion et le nombre de victimes.
