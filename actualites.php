	<div id="article">
		<h2>Le virus Ebola : peut-être un remède ?</h2>
		Le 27 novembre 2011 les résultats d’un test de vaccin pour le virus Ebola ont été publiés sur lepoint.fr. Le test s’est déroulé aux Etats-Unis et il semble très prometteur. Testé sur une vingtaine de personne, le vaccin ChAd3, à été très bien supporté et a déclenché de bonnes défenses immunitaires.
		Le vaccin ChAd3 contient des éléments génétiques provenant de deux souches du virus Ebola (Soudan et Zaïre), acheminés par un adénovirus responsable du rhume chez les chimpanzés, un agent inoffensif pour l'homme.
		Mais selon le Dr Michael Kurilla, du NIAID (developpeur du vaccin), l’avenir de ces vaccins est encore incertain car il faut encore faire de nombreux tests qui seront réaliser d’ici milieu 2015.
		En attendant ces vaccins, des traitements expérimentaux ont été administrés à des malades d'Ebola à titre compassionnel.
